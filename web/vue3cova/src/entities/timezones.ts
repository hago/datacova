export interface TimeZone {
    name: string,
    offset: number
}

export const getTimeZones = (d: Date): string[] => {
    let off = d.getTimezoneOffset() * 60 * 1000
    console.log('time offset: ', off)
    let matches = timezones.filter(tz => tz.offset === off)
    return matches.map(tz => tz.name)
}

export const getTimeZone = (d: Date): string => {
    let l = getTimeZones(d)
    return l.length > 0 ? l[0] : "UTC"
}


